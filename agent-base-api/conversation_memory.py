def conversation_context(session_id: str, limit_turns: int = 4) -> dict:
    """Lightweight summary for the synthesizer prompt."""
    session = get_session(session_id) or {}
    turns = recent_turns(session_id, limit=limit_turns)
    return {
        "session_id": session_id,
        "active_entity_label": session.get("active_entity_label"),
        "active_entity_type": session.get("active_entity_type"),
        "active_intent": session.get("active_intent"),
        "history": [
            {
                "question": t.get("question"),
                "resolved_question": t.get("resolved_question"),
                "intent": t.get("routed_intent") or t.get("intent"),
                "entity": t.get("primary_entity_label"),
            }
            for t in turns
        ],
        "anti_phrases": anti_phrase_list(session_id),
    }