def open_session(user_id: int, session_id: str | None = None) -> dict:
    """Idempotent: returns the session row, creating it if needed."""
    sid = session_id or f"sess_{uuid.uuid4().hex[:16]}"
    row = execute_query(
        "SELECT * FROM chat_sessions WHERE id=? AND user_id=?",
        (sid, int(user_id)),
        one=True,
    )
    if row:
        return _row_to_session(row)

    ts = now_iso()
    execute_write(
        """
        INSERT INTO chat_sessions (
            id, user_id, opened_at, last_turn_at, meta_json
        )
        VALUES (?, ?, ?, NULL, '{}')
        """,
        (sid, int(user_id), ts),
    )
    return {
        "id": sid,
        "user_id": int(user_id),
        "opened_at": ts,
        "last_turn_at": None,
        "active_entity_type": None,
        "active_entity_id": None,
        "active_entity_label": None,
        "active_intent": None,
        "meta": {},
    }


def get_session(session_id: str) -> dict | None:
    row = execute_query(
        "SELECT * FROM chat_sessions WHERE id=?",
        (session_id,),
        one=True,
    )
    return _row_to_session(row) if row else None


def _row_to_session(row) -> dict:
    d = dict(row)
    raw = d.pop("meta_json", None) or "{}"
    try:
        d["meta"] = json.loads(raw)
    except json.JSONDecodeError:
        d["meta"] = {}
    return d


# ---------------------------------------------------------------------------
# Phrase signatures — anti-repetition fuel
# ---------------------------------------------------------------------------


_BOILERPLATE = {
    "şu", "şuan", "şu an", "anda", "bir", "ve", "ile", "için", "ama", "fakat",
    "olabilir", "görünüyor", "yapabilirim", "hazırlayabilirim",
    "küçük", "kısa", "iyi", "değerli",
}


def extract_phrase_signatures(text: str) -> list[str]:
    """Return short distinctive phrases (3..6 word windows) to forbid repeating.

    We don't grab the whole sentence — we grab the *opening* of each line and
    each bulleted recommendation. Those are the patterns that get noticed
    when a synthesizer repeats itself ("küçük bir indirim kampanyası" again
    and again, etc.).
    """
    if not text:
        return []
    sigs: list[str] = []
    for line in text.split("\n"):
        s = line.strip(" •-—*").strip()
        if not s:
            continue
        words = s.split()
        if len(words) < 3:
            continue
        # opening 3..6 words is the recognizable "tic"
        for window in (3, 4, 5):
            if len(words) >= window:
                opener = " ".join(words[:window]).lower()
                # filter out openers that are essentially boilerplate
                if sum(1 for w in opener.split() if w in _BOILERPLATE) >= window - 1:
                    continue
                sigs.append(opener)
                break
    # de-duplicate keeping order
    seen: set[str] = set()
    out: list[str] = []
    for s in sigs:
        if s not in seen:
            seen.add(s)
            out.append(s)
    return out


def answer_signature(text: str) -> str:
    """Stable hash for "have I said this answer recently" detection."""
    norm = re.sub(r"\s+", " ", (text or "").strip().lower())
    return hashlib.sha1(norm.encode("utf-8")).hexdigest()[:16]


