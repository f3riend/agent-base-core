def record_turn(
    *,
    session_id: str,
    user_id: int,
    question: str,
    resolved_question: str | None,
    intent: str | None,
    routed_intent: str | None,
    primary_entity_type: str | None,
    primary_entity_id: int | None,
    primary_entity_label: str | None,
    answer: str,
    confidence: float | None,
) -> int:
    """Persist a turn and update session 'active' fields."""
    sigs = extract_phrase_signatures(answer)
    sig = answer_signature(answer)
    ts = now_iso()

    turn_id = execute_write(
        """
        INSERT INTO chat_turns (
            session_id, user_id, question, resolved_question,
            intent, routed_intent,
            primary_entity_type, primary_entity_id, primary_entity_label,
            answer, answer_signature, phrase_signatures_json,
            confidence, created_at
        )
        VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        """,
        (
            session_id, int(user_id), question, resolved_question,
            intent, routed_intent,
            primary_entity_type, primary_entity_id, primary_entity_label,
            answer, sig, json.dumps(sigs),
            float(confidence) if confidence is not None else None,
            ts,
        ),
    )

    execute_write(
        """
        UPDATE chat_sessions
        SET last_turn_at=?,
            active_entity_type=COALESCE(?, active_entity_type),
            active_entity_id=COALESCE(?, active_entity_id),
            active_entity_label=COALESCE(?, active_entity_label),
            active_intent=COALESCE(?, active_intent)
        WHERE id=?
        """,
        (
            ts,
            primary_entity_type,
            primary_entity_id,
            primary_entity_label,
            routed_intent or intent,
            session_id,
        ),
    )
    return turn_id


def set_active_rule(session_id: str, rule_id: int | None, rule_name: str | None = None) -> None:
    """Conversational rule edit için session'ın "aktif kuralını" işaretle.

    Operatör "bu kuralı kapat" dediğinde rule_edit modülü session'dan
    bu rule_id'yi alıp işlem yapar.
    """
    if not session_id:
        return
    try:
        execute_write(
            """
            UPDATE chat_sessions
            SET active_rule_id=?, active_rule_name=?
            WHERE id=?
            """,
            (int(rule_id) if rule_id else None, rule_name, session_id),
        )
    except Exception as exc:
        print(f"[CONV_MEMORY] set_active_rule failed: {exc}")


def get_active_rule(session_id: str) -> tuple[int | None, str | None]:
    """Session'ın aktif rule_id ve name'ini döndürür (yoksa None, None)."""
    if not session_id:
        return None, None
    row = execute_query(
        "SELECT active_rule_id, active_rule_name FROM chat_sessions WHERE id=?",
        (session_id,), one=True,
    )
    if not row:
        return None, None
    return (
        int(row["active_rule_id"]) if row["active_rule_id"] is not None else None,
        row["active_rule_name"],
    )


def recent_turns(session_id: str, limit: int = 6) -> list[dict]:
    rows = execute_query(
        """
        SELECT * FROM chat_turns
        WHERE session_id=?
        ORDER BY id DESC LIMIT ?
        """,
        (session_id, int(limit)),
    )
    out = []
    for r in rows:
        d = dict(r)
        try:
            d["phrase_signatures"] = json.loads(d.pop("phrase_signatures_json", "[]") or "[]")
        except json.JSONDecodeError:
            d["phrase_signatures"] = []
        out.append(d)
    return list(reversed(out))


def anti_phrase_list(session_id: str, limit_turns: int = 4) -> list[str]:
    """Phrases the LLM should avoid repeating in the next turn."""
    turns = recent_turns(session_id, limit=limit_turns)
    phrases: list[str] = []
    seen: set[str] = set()
    for t in turns:
        for p in t.get("phrase_signatures") or []:
            if p not in seen:
                seen.add(p)
                phrases.append(p)
    return phrases[:12]


