"""
Conversation memory for the AI operator chat.

The current chat is single-turn: every question is answered in isolation.
The user wants multi-turn cognition — "peki neden?" / "onu öne çıkar" /
"bunun için bir kampanya hazırla" — must resolve against the previous
turn's subject without the user repeating it.

This module owns:
    - chat_sessions: a session is a chat conversation. Many users / many
      sessions per user; a session_id is generated by the dashboard and
      stored in localStorage.
    - chat_turns: every (question, intent, primary_entity, response
      signature) tuple in chronological order.
    - resolve_follow_up(): detects short follow-up references ("neden",
      "peki", "onu", "bunu") and rewrites the question into a full
      stand-alone form using the previous turn's entity + intent.
    - anti_phrase_list(): returns the 5–8 sentences the synthesizer
      produced most recently in this session, so the LLM can be instructed
      to avoid repeating them.

This is in-process and SQLite-backed. No external service.
"""

from __future__ import annotations

