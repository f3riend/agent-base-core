# Short fragments that mean "continue with the previous subject".
_FOLLOWUP_HINTS = (
    "peki",        # peki neden? / peki nasıl?
    "neden",       # neden? (standalone)
    "niye",
    "nasıl",
    "ya da",
    "onu",
    "bunu",
    "bu ürün",
    "bu kampanya",
    "öne çıkar",
    "öne çıkaralım",
    "kampanya hazırla",
    "bir fikir",
    "fikir üret",
    "bir öneri",
    "anlat",
    "açıkla",
    "detay",
    "devam",
    "tekrar",
    "peki ya",     # peki ya diğeri?
    "ya diğeri",
    "ya öteki",
    "diğeri",
    "öteki",
    "az önce",     # az önce ne dedin?
    "daha detay",
    "daha fazla",
)


_QUESTION_WORD_RE = re.compile(
    r"\b(ürün|product|kampanya|campaign|mağaza|store|sipariş|order|"
    r"yorum|review|stok|stock|satış|sales|müşteri|customer)\b",
    re.IGNORECASE,
)


def _looks_like_followup(question: str) -> bool:
    q = (question or "").strip().lower()
    if not q:
        return False
    # Standalone subject already in question → not a follow-up
    if _QUESTION_WORD_RE.search(q):
        # Unless it's *just* "bu ürün için" + something short
        if not q.startswith(("bu ürün", "bu kampanya", "o ürün", "şu ürün")):
            return False
    # Heuristic: short question (≤ 6 words) or starts with a follow-up hint
    word_count = len(q.split())
    if word_count <= 4:
        return True
    if any(q.startswith(hint) for hint in _FOLLOWUP_HINTS):
        return True
    if any(q == hint or q == hint + "?" for hint in _FOLLOWUP_HINTS):
        return True
    return False


@dataclass
class FollowUpResolution:
    is_followup: bool
    resolved_question: str        # the question to feed to retrieval
    inherited_entity_label: str | None
    inherited_intent: str | None
    rationale: str                # for logging / dashboard chip


def resolve_follow_up(question: str, session_id: str) -> FollowUpResolution:
    """If the question looks like a follow-up, rewrite it using session state."""
    if not session_id:
        return FollowUpResolution(False, question, None, None, "no_session")

    session = get_session(session_id)
    if not session or not session.get("last_turn_at"):
        return FollowUpResolution(False, question, None, None, "fresh_session")

    if not _looks_like_followup(question):
        return FollowUpResolution(False, question, None, None, "standalone_question")

    active_label = session.get("active_entity_label")
    active_intent = session.get("active_intent")
    if not active_label and not active_intent:
        return FollowUpResolution(False, question, None, None, "no_active_context")

    q = (question or "").strip().rstrip("?")

    # "peki ya diğeri" / "ya öteki" → diğer ürün/varlık hakkında sor
    if any(k in q.lower() for k in ("diğeri", "öteki", "peki ya", "ya diğeri")):
        if active_label:
            rewritten = f"{active_label} dışındaki diğer ürün hakkında bilgi ver"
        else:
            rewritten = "Diğer ürün hakkında bilgi ver"
        return FollowUpResolution(
            True, rewritten, None, active_intent,
            "other_entity_lookup",
        )

    # "az önce ne dedin" / "tekrar söyle" → önceki cevabı özetle
    if any(k in q.lower() for k in ("az önce", "tekrar söyle", "ne dedin")):
        if active_label:
            rewritten = f"{active_label} hakkında az önce söylediklerini özetle"
        else:
            rewritten = "Az önce söylediklerini özetle"
        return FollowUpResolution(
            True, rewritten, active_label, active_intent,
            "recap_last_turn",
        )

    # "neden" / "peki neden" → "why" lookup for the active subject
    if q in ("neden", "peki", "peki neden") or q.startswith(("peki neden", "neden ")):
        if active_intent and "sales" in active_intent.lower():
            rewritten = "Satışlar neden bu durumda?"
        elif active_label:
            rewritten = f"{active_label} için durumun arkasındaki sebepler ne?"
        else:
            rewritten = "Bunun arkasındaki sebepler ne?"
        return FollowUpResolution(
            True, rewritten, active_label, active_intent,
            f"why_lookup({active_intent or '-'})",
        )

    # "onu öne çıkaralım" / "kampanya hazırla" → action proposal for active entity
    if any(k in q for k in ("öne çıkar", "kampanya hazırla", "instagram", "tanıt", "duyur")):
        if active_label:
            rewritten = f"{active_label} için yeni bir kampanya akışı önerebilir misin?"
        else:
            rewritten = "Aktif konu için bir kampanya akışı önerir misin?"
        return FollowUpResolution(
            True, rewritten, active_label, "growth_marketing",
            f"action_proposal({active_label or '-'})",
        )

    # "bir fikir üret" / "bir öneri" → generic recommendation around active entity
    if any(k in q for k in ("fikir", "öneri")):
        if active_label:
            rewritten = f"{active_label} için strateji önerisi"
        else:
            rewritten = "Mevcut bağlam için strateji önerisi"
        return FollowUpResolution(
            True, rewritten, active_label, "insights",
            f"recommend_around({active_label or '-'})",
        )

    # generic "anlat / açıkla / detay" → describe the active subject
    if any(k in q for k in ("anlat", "açıkla", "detay", "daha fazla")):
        if active_label:
            rewritten = f"{active_label} hakkında biraz daha detay"
        else:
            rewritten = "Bu konu hakkında daha fazla bilgi"
        return FollowUpResolution(
            True, rewritten, active_label, active_intent,
            f"elaborate({active_label or '-'})",
        )

    # Default: append the active entity to the short question
    if active_label and active_label.lower() not in q:
        rewritten = f"{question.strip()} ({active_label})"
        return FollowUpResolution(
            True, rewritten, active_label, active_intent,
            f"append_entity({active_label})",
        )

    return FollowUpResolution(False, question, None, None, "no_rewrite_path")


